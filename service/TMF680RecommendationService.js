exports.tMF680retrieveRecommendation = function(req, res, next) {
  /**
   * Retrieves a 'Recommendation' by Id
   *
   * id String Identifier of the Recommendation
   * returns List
   **/

  console.log('tMF680retrieveRecommendation :: ' + req.method + ' ' + req.url + ' ' + req.headers.host);

  /* matching isRestfulShow */

  var id = String(req.swagger.params.id.value);

  var query = mongoUtils.getMongoQuery(req);
  query.criteria.id = id

  const resourceType = getResourceType(req); 

  const internalError =  new TError(TErrorEnum.INTERNAL_SERVER_ERROR, "Internal database error");

  mongoUtils.connect().then(db => {
    db.collection(resourceType)
      .findOne(query.criteria, query.options)
      .then(doc => {
        if(doc) {
          sendDoc(res, 200, doc)
        } else {
          sendError(res, new TError(TErrorEnum.RESOURCE_NOT_FOUND,"No resource with given id found"));
        }
      })
      .catch(error => {
        console.log("tMF680retrieveRecommendation: error=" + error);
        sendError(res, internalError);
      });
  })
  .catch(error => {
    console.log("tMF680retrieveRecommendation: error=" + error);
    sendError(res, internalError);
  });




};

